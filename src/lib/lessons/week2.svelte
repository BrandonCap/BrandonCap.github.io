		<h3>How do software licenses work?</h3>
		<p>
			Software licenses state the right of the developer and the user of the software. Some examples of these terms are:
		</p>
		<ul>
			<li>how many times the software can be downloaded</li>
			<li>what the software will cost</li>
			<li>what level of access users have to the source code</li>
		</ul>
		<p>
			These terms are typically outlined in the "end-user license agreement" or an "enterprise license agreement".
		</p>
		<br>

		<h3>What are the different types of software licenses?</h3>
		<p>There are two main types as mentioned before: free/open source and proprietary licenses.</p>
		<ul>
			<li><b>Free and open source software (FOSS) licenses</b> are commonly known as open source. This license makes the source code available to the customer, and usually allows them to modify it.</li>
			<li><b>Proprietary licenses</b> are known as closed source. Only operational code is provided, and reverse engineering the software to obtain source code is typically restricted.</li>
		</ul>
		<br>

